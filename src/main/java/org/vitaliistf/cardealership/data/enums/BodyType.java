/**
 * Represents the body types of a car.
 */
public enum BodyType {
    /** Sedan body type. */
    SEDAN,
    /** Hatchback body type. */
    HATCHBACK,
    /** Coupe body type. */
    COUPE,
    /** Minivan body type. */
    MINIVAN,
    /** Convertible body type. */
    CONVERTIBLE,
    /** Sport body type. */
    SPORT,
    /** SUV (Sport Utility Vehicle) body type. */
    SUV,
    /** Pickup body type. */
    PICKUP,
    /** Wagon body type. */
    WAGON
}
